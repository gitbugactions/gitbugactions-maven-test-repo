package com.testrepo;

public class AppMath {
    public AppMath() {
    }

    public int sum(int a, int b) {
        return a + b;
    }

    public int subtract(int a, int b) {
        return a - b;
    }

    public int multiply(int a, int b) {
        return a * b;
    }

    public int sumTwice(int a, int b) {
        return a + b + b;
    }

    public int subtractTwice(int a, int b) {
        return a - b - b;
    }
}
